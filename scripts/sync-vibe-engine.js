#!/usr/bin/env node
'use strict';

/**
 * sync-vibe-engine.js — Copy lib/vibeEngine.ts to the Deno edge function copy.
 *
 * Fixes imports from '@/types/vibeProfile' to './vibeProfile.ts' for Deno compatibility.
 *
 * Run after any changes to the vibe engine source file.
 *
 * Usage:
 *   node scripts/sync-vibe-engine.js
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SRC = path.join(ROOT, 'lib/vibeEngine.ts');
const OUT = path.join(ROOT, 'supabase/functions/_shared/vibeEngine.ts');

let src = fs.readFileSync(SRC, 'utf8');

// Fix imports for Deno
src = src.replace(/from '@\/types\/vibeProfile'/g, "from './vibeProfile.ts'");
src = src.replace(/from "@\/types\/vibeProfile"/g, "from './vibeProfile.ts'");

// Add header
const header = `/**
 * Vibe Engine — DEPLOY COPY for Supabase Edge Functions (Deno runtime).
 * AUTO-GENERATED by scripts/sync-vibe-engine.js — DO NOT EDIT DIRECTLY.
 * SOURCE OF TRUTH: lib/vibeEngine.ts
 */\n\n`;

fs.writeFileSync(OUT, header + src);
console.log(`\u2705 Synced vibeEngine.ts (${src.length} bytes) \u2192 ${path.relative(ROOT, OUT)}`);
